<b>LATAR BELAKANG DARI PERISTIWA KESAKTIAN PANCASILA</b>
    <hr>
    <h6>Kedudukan Pancasila sebagai dasar negara sempat mengalami berbagai tantangan sepanjang sejarahnya. Salah satu peristiwa yang sangat berpengaruh terhadap pengakuan dan penghormatan terhadap Pancasila adalah Tragedi G30S PKI. Pada peristiwa tragis ini, enam perwira tinggi dan satu perwira menengah TNI AD tewas dalam upaya kudeta yang dilakukan oleh PKI.

Tragedi G30S PKI merupakan salah satu babak kelam dalam sejarah bangsa Indonesia. Percobaan kudeta ini bertujuan untuk menggulingkan pemerintahan Presiden Soekarno dan menjadikan Indonesia sebagai negara komunis. Peristiwa ini juga menandai upaya PKI untuk mengubah Pancasila menjadi ideologi komunis.</h6>

<br>
<h6>Dalam peristiwa ini, beberapa perwira angkatan darat dan sejumlah orang lainnya menjadi korban pembunuhan yang dianggap sebagai upaya perebutan kekuasaan. Hingga saat ini, peristiwa G30S PKI masih menjadi perdebatan di kalangan akademisi mengenai pelakunya dan motif di baliknya.

Untuk mengenang peristiwa yang hampir mengancam eksistensi ideologi bangsa Indonesia, pemerintahan Orde Baru menetapkan 1 Oktober sebagai Hari Kesaktian Pancasila melalui Surat Keputusan Menteri/Panglima Angkatan Darat tanggal 17 September 1966</h6>
<br>

<b>KENAPA HARUS TANGGAL 1 JUNI?</b>
<hr>
<h6>Penetapan 1 Oktober sebagai hari Kesaktian Pancasila dilakukan oleh Jenderal Soeharto. Untuk Pancasila sendiri, ideologi ini lahir pada 1 Juni 1945, atas inisiatif Soekarno dalam rapat Panitia Sembilan.</h6>
<br>
<h6>Meski begitu, keberadaan ideologi ini tidak menjadi pusaka yang begitu sakti. Karena Pancasila lahir secara wajar dan sesuai keadaan yang begitu obyektif saat itu. Akan tetapi dalam perkembangannya, Pancasila kemudian mampu diterima bangsa Indonesia sebagai dasar negara. Di pihak lain, terjadi pemberontakan dan pembentukan beberapa dewan, yang kemudian mampu dihancurkan oleh dukungan rakyat saat itu</h6>
