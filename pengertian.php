<b>Pengertian Hari Kesaktian Pancasila</b>
<hr>
<h6>Hari Kesaktian Pancasila adalah peringatan yang dilakukan setiap tanggal 1 Oktober sebagai bentuk penghormatan terhadap para pahlawan yang gugur dalam menjaga keutuhan dan kejayaan Pancasila.

Melalui peringatan ini, kita diminta untuk merefleksikan arti kesaktian Pancasila dan pentingnya menjaga dan mempertahankan nilai-nilai luhur yang terkandung di dalamnya</h6>
<br>
<h6>Peringatan Hari Kesaktian Pancasila ini untuk mengenang gugurnya pahlawan revolusi dalam Gerakan 30 September (G30S) pada tahun 1965. Selain itu, peringatan ini juga untuk mengingatkan masyarakat soal ideologi Pancasila yang tak bisa digantikan oleh paham apapun.

Pada tahun ini, tema yang diusung dalam Upacara Hari Kesaktian Pancasila adalah “Pancasila Pemersatu Bangsa menuju Indonesia Maju”.

Penting untuk diketahui, Hari Kesaktian Pancasila ini berbeda dengan Hari Lahir Pancasila yang diperingati setiap 1 Juni.

Hari lahir Pancasila merupakan rumusan dasar negara yang disampaikan oleh Soekarno pada sidang Badan Penyelidik Usaha Persiapan Kemerdekaan Indonesia (BPUPKI).

Tanggal 1 Juni resmi ditetapkan jadi Hari Lahir Pancasila lewat Keputusan Presiden Nomor 24 Tahun 2016 Presiden Joko Widodo (Jokowi)</h6>
<br>
<br>

<b>Makna Hari Kesaktian Pancasila</b>
<hr>
<h6>1. Penghormatan terhadap Pahlawan</h6>
<h6>Hari Kesaktian Pancasila adalah momen untuk menghormati dan mengenang seluruh pahlawan yang telah gugur dalam melindungi Pancasila. Mereka telah berjuang dengan gigih untuk mempertahankan nilai-nilai Pancasila sebagai dasar negara dan ideologi bangsa.</h6>
<h6>2. Perjuangan untuk Mempertahankan Pancasila</h6>
<h6>Peringatan ini mengingatkan kita akan perjuangan para pahlawan dalam membentengi peranan Pancasila sebagai dasar negara dan ideologi bangsa. Pancasila bukan hanya sekadar kumpulan kata-kata, tetapi juga landasan yang mengikat bangsa Indonesia dalam menjalankan kehidupan berbangsa dan bernegara.</h6>
<h6>3. Meningkatkan Nasionalisme dan Patriotism</h6>
<h6>Bertujuan untuk membangkitkan kembali rasa nasionalisme dan patriotisme yang mungkin telah mulai luntur. Dalam menghadapi berbagai tantangan dan perubahan zaman, menjaga dan mencintai Pancasila sebagai identitas nasional adalah kunci untuk membangun bangsa yang kuat dan bersatu.</h6>
