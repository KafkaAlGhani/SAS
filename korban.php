<b>BERIKUT ADALAH KORBAN KORBAN DARI PERISTIWA G30SPKI</b>
<hr>
<h6>1. Jenderal (Anumerta) Ahmad Yani</h6>
<br>
<h6>Ahmad Yani merupakan salah satu korban yang tewas dalam pemberontakan G30S PKI, tepatnya tanggal 1 Oktober 1965. Ia terbunuh akibat difitnah ingin menjatuhkan Presiden Soekarno pada tahun yang sama.</h6>
<br>
<h6>Ketika mengembuskan napas terakhirnya, Ahmad Yani berpangkat sebagai Kepala Staf Angkatan Darat (KSAD) yang didapatnya pada tahun 1962. Sebelumnya, pada 1958 ia diangkat sebagai Komandan Komando Operasi 17 Agustus di Padang, Sumatera barat untuk memberantas pemberontakan Pemerintahan Revolusioner Republik Indonesia (PRRI).</h6>
<br>
<br>
<h6>2. Letjen (Anumerta) Suprapto</h6>
<h6>Suprapto merupakan pejuang kelahiran Purwokerto pada 20 Juni 1920. Karirnya di dunia militer dimulai dengan masuk Akademi Militer Kerajaan Bandung. Sayangnya, pendidikannya tersebut harus berhenti karena pendaratan Jepang di Indonesia.
Namun, pada awal kemerdekaan Indonesia, Suprapto turut serta dalam usaha bangsa Indonesia merebut senjata pasukan Jepang di Cilacap. Setelahnya, ia melanjutkan karir militernya dengan masuk Tentara keamanan Rakyat (TKR) di Purwokerto.</h6>

<h6>Saat berkarir di TKR, Suprapto dipercaya sebagai ajudan Panglima Besar Sudirman dalam pertempuran di Ambarawa. Karir Suprapto di militer pun semakin melejit.
Di tengah puncak karirnya, PKI mengajukan pembentukan angkatan perang kelima, namun Suprapto menolaknya. Nahasnya, ia pun menjadi korban pemberontakan G30S PKI yang jasadnya ditemukan di Lubang Buaya.</h6>
<br>
<h6>3. Letjen (Anumerta) S. Parman</h6>
<br>
<h6>Siswondo S Parman atau akrab disapa S. Parman merupakan petinggi TNI AD orde lama yang berkutat di bidang intelijen. Pria kelahiran Jawa Tengah pada 4 Agustus 1918 itu pernah dikirim ke Jepang untuk memperdalam ilmu intelijen pada Kenpei Kasya Butai</h6>
<h6>Begitu Indonesia memproklamasikan kemerdekaannya, S. Parman pun memilih mengabdi kepada negara untuk memperkuat militer Indonesia. Terlebih lagi, pengalamannya di bidang intelijen sangat berguna bagi TNI pada masa itu.
Dengan keahliannya, S. Parman berhasil mengetahui rencana yang diusung PKI yang ingin membentuk angkatan kelima. Karena hal itu, ia diculik dan harus gugur pada tanggal 1 Oktober 1965.
<br>
<h6>4. Letjen (Anumerta) M.T. Haryono</h6>
<br>
<h6>Berbeda dengan pahlawan revolusi lainnya, Mas Tirtodarmo Haryono atau M.T. Haryono memiliki latar belakang pendidikan di Ika Dai Gaku yaitu sekolah kedokteran. Setelah kemerdekaan Indonesia, barulah ia terjun ke dunia militer dengan bergabung bersama TKR dan menyandang pangkat mayor.</h6>
<h6>M.T. Haryono dikenal sebagai sosok yang piawai dalam berbahasa Belanda, Inggris, dan Jerman. Bakatnya itu pun dimanfaatkan oleh Indonesia untuk melakukan berbagai perundingan internasional.
M.T. Haryono kemudian berkarir di Kementerian Pertahanan dan sempat menjabat sebagai Sekretaris Delegasi Militer Indonesia. Setelahnya, pada tahun 1950, ia menjadi Atase Militer RI untuk Negeri Belanda.</h6>
<br>
<h6>5. Mayjen (Anumerta) D.I. Panjaitan</h6>
<br>
<h6>Donald Ignatius Panjaitan atau lebih dikenal dengan D.I. Panjaitan adalah pahlawan yang lahir pada 9 Juni 1925 di Balige, Tapanuli. Ia memulai karir di dunia militer dengan memasuki pendidikan militer Gyugun saat Indonesia masih diduduki oleh Jepang.</h6>
<h6>D.I. Panjaitan kemudian ditempatkan di Pekanbaru, Riau sampai Indonesia memproklamasikan kemerdekaannya. Setelah itu, ia pun turut serta membentuk TKR dan memiliki karir cemerlang di bidang ini.
Sebelum gugur dalam pemberontakan G30S PKI, D.I. Panjaitan sempat diangkat sebagai Asisten IV Menteri/Panglima Angkatan Darat. Bahkan, ia mendapat tugas belajar di Amerika Serikat.
</h6>
<br>
<h6>6. Mayjen (Anumerta) Sutoyo Siswomiharjo</h6>
<br>
<h6>Pada masa penjajahan Jepang, Sutoyo mulanya menempuh pendidikan di Balai Pendidikan Pegawai Tinggi di Jakarta. Pria kelahiran 28 Agustus 1922 di Kebumen, Jawa Tengah ini kemudian menjadi seorang pegawai negeri pada Kantor Kabupaten di Purworejo.</h6>
<h6>Setelah kemerdekaan Indonesia diproklamasikan, ia bergabung dalam TKR bagian kepolisian. Sutoyo menjadi anggota Korps Polisi Militer.
Karirnya lantas melejit dengan diangkat menjadi ajudan Kolonel gatot Subroto. Setelahnya, Sutoyo menjadi Kepala bagian Organisasi Resimen II Polisi Tentara di Purworejo.</h6>
<h6>Sutoyo kemudian diberi tugas sebagai Inspektur Kehakiman/Oditur Jenderal Angkatan Darat pada tahun 1961. Ketika PKI mengusung pembentukan angkatan kelima, Sutoyo menentangnya dan akhirnya ikut gugur dalam pemberontakan G30S PKI.
</h6>
<br>
<h6>7. Kapten (Anumerta) Pierre Tendean</h6>
<br>
<h6>Pierre Tendean lulus dari Akademi Militer Jurusan Teknik pada tahun 1962. Pria kelahiran Jakarta 21 Februari 1939 ini menjabat sebagai Komandan Peleton Batalyon Zeni Tempur 2 Komando daerah Militer II/ Bukit Barisan di Medan</h6>
<h6>Pierre Tendean juga turut serta menyusup ke daerah malaysia saat sedang berkonfrontasi dengan negara tersebut. Hingga pada April 1965, Pierre Tendean yang masih seorang perwira muda itu diangkat sebagai ajudan Menteri Koordinator Pertahanan Keamanan/ kepala Staf Angkatan Bersenjata Jenderal Nasution.</h6>
